# Mapping in Python
arr = list(map(int, input().split()))
print("Mapped array:", arr)

# Example usage
# Input: 1 2 3 4 5
# Output: Mapped array: [1, 2, 3, 4, 5]
# This code reads a line of input, splits it into individual string elements,
# converts each element to an integer using map, and then constructs a list
# from the mapped integers.# The resulting list is then printed to the
# console.